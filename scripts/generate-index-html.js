#!/usr/bin/env node
// Reads dist/index.html (produced by `npx expo export --platform web`)
// and writes api/_lib/indexHtml.ts exporting its contents as a TS string
// constant. The HTML-injection Vercel Functions import that constant
// instead of reading the file with fs at runtime — which avoids the
// includeFiles glob entirely and guarantees the HTML is in every
// function bundle that imports it.
//
// Runs as part of the Vercel buildCommand after `expo export`.

const fs = require('fs');
const path = require('path');

const srcPath = path.join(__dirname, '..', 'dist', 'index.html');
const destPath = path.join(__dirname, '..', 'api', '_lib', 'indexHtml.ts');

if (!fs.existsSync(srcPath)) {
  console.error(`[generate-index-html] source not found: ${srcPath}`);
  process.exit(1);
}

const html = fs.readFileSync(srcPath, 'utf-8');

// Escape the HTML for inclusion inside a JS template literal. We need to
// escape backticks, dollar-signs followed by braces, and backslashes.
const escaped = html
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$\{/g, '\\${');

const out = `// AUTO-GENERATED by scripts/generate-index-html.js — DO NOT EDIT.
// Source: dist/index.html after \`expo export --platform web\`.
// Run via the Vercel buildCommand; committed so Jest doesn't need to
// regenerate. Update by running the script locally or triggering a
// fresh Vercel build.

export const INDEX_HTML = \`${escaped}\`;
`;

fs.mkdirSync(path.dirname(destPath), { recursive: true });
fs.writeFileSync(destPath, out, 'utf-8');
console.log(`[generate-index-html] wrote ${destPath} (${html.length} chars)`);
